    "    elif path.endswith('.txt'):\n",
    "        text = extract_text_from_txt(path)\n",
    "    else:\n",
    "        continue\n",
    "\n",
    "    if text:\n",
    "        paper_name = path.split('/')[-1].replace('.pdf', '').replace('.txt', '')\n",
