    "# Now we start reading the papers\n",
    "# Depending on PDF or txt\n",
    "\n",
    "def extract_text_from_pdf(pdf_path):\n",
    "    \"\"\"Extract text from PDF file\"\"\"\n",
    "    try:\n",
    "        import pymupdf  # or PyPDF2\n",
    "        doc = pymupdf.open(pdf_path)\n",
    "        text = ''\n",
    "        for page in doc:\n",
    "            text += page.get_text()\n",
    "        return text\n",
    "    except Exception as e:\n",
    "        print(f'Error reading {pdf_path}: {e}')\n",
    "        return None\n",
    "\n",
    "def extract_text_from_txt(txt_path):\n",
    "    \"\"\"Extract text from TXT file\"\"\"\n",
    "    try:\n",
    "        with open(txt_path, 'r', encoding='utf-8') as f:\n",
    "            return f.read()\n",
    "    except Exception as e:\n",
    "        print(f'Error reading {txt_path}: {e}')\n",
    "        return None\n",
    "\n",
    "# Create paper_texts dictionary\n",
    "paper_texts = {}\n",
