    "# Clean the text data\n",
    "def clean_text(text):\n",
    "    \"\"\"Clean text for NLP analysis\"\"\"\n",
    "    # Lowercase\n",
    "    text = text.lower()\n",
    "\n",
    "    # Remove punctuation\n",
    "    text = re.sub(r'[^\\w\\s]', '', text)\n",
    "\n",
    "    # Remove numbers\n",
    "    text = re.sub(r'\\d+', '', text)\n",
    "\n",
    "    # Remove URLs\n",
    "    text = re.sub(r'http\\S+|\\S+\\.com\\S+', '', text)\n",
    "\n",
    "    # Remove citations (e.g., [12], (2023))\n",
    "    text = re.sub(r'\\[\\d+\\]', '', text)\n",
    "    text = re.sub(r'\\(\\d{4}\\)', '', text)\n",
    "\n",
    "    # Normalize whitespace\n",
    "    text = re.sub(r'\\s+', ' ', text).strip()\n",
    "\n",
    "    return text\n",
    "\n",
    "# Clean all papers\n",
    "paper_texts_cleaned = {}\n",
    "for paper_name, text in paper_texts.items():\n",
    "    paper_texts_cleaned[paper_name] = clean_text(text)\n",
    "\n",
    "print(f'Cleaned {len(paper_texts_cleaned)} papers')\n",
    "print(f'\\nExample cleaned text (first 200 chars):')\n",
    "print(paper_texts_cleaned[list(paper_texts_cleaned.keys())[0]][:200])"
